package level1Assignment.Loops;

public class Qn3Loops {
    public static void main(String[] args) {
        int num1=24;
        int num2=50;
        int num3=29;
        System.out.println("Multiplication of "+num1+" is:");
        for(int i=1;i<=10;i++)
            System.out.println(num1 +"*"+i+"="+ num1*i);
        System.out.println();
        System.out.println("Multiplication of "+num2+" is:");
        for(int i=1;i<=10;i++)
            System.out.println(num2 +"*"+i+"="+ num2*i);
        System.out.println();
        System.out.println("Multiplication of "+num3+" is:");
        for(int i=1;i<=10;i++)
            System.out.println(num3 +"*"+i+"="+ num3*i);
    }
}
